def get_round_directive(
    round_num: int,
    status: str = "",
    used_questions: list[str] | None = None,
) -> str:
    """Return the LLM directive for the given round."""
    used = used_questions or []

    if round_num == 1:
        question = pick_question(Dimension.SEPARATION_ANXIETY, used)
        return f"{_STATUS_ROUND_DIRECTIVE}\n\u53c2\u8003\u60c5\u666f\u9898\uff1a{question}"

    dim = get_dimension_for_round(round_num) or Dimension.NEEDS_EXPRESSION
    base = _DIMENSION_DIRECTIVES[dim]
    question = pick_question(dim, used)
    status_note = f"\n\u7528\u6237\u604b\u7231\u72b6\u6001\uff1a{status}" if status else ""
    return f"{base}{status_note}\n\u53c2\u8003\u60c5\u666f\u9898\uff08\u53ef\u636e\u5bf9\u8bdd\u8c03\u6574\uff09\uff1a{question}"


def is_final_round(round_num: int) -> bool:
    return round_num >= FINAL_ROUND


def is_status_round(round_num: int) -> bool:
    return round_num == 1